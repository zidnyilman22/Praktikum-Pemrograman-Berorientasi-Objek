public class Tugas1StringDays {

    public static void main(String[] args) {
        // Mendeklarasikan dan menginisialisasi array String dengan 7 hari dalam seminggu.
        String days[] = {"Monday", "Tuesday", 
                        "Wednesday", "Thursday", 
                        "Friday", "Saturday", "Sunday"};

        //menampilkan semua hari dalam array.
        for (String day : days) {
            System.out.println(day);
        }
    }
}
